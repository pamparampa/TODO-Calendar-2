package com.example.radle.todo_calendar2.calendarView;

public class ClickPoint {
    private final float x;
    private final float y;

    public ClickPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
